# visible marker for any glyph we forgot to draw
MISSING = ["#.#.#", ".#.#.", "#.#.#", ".#.#.", "#.#.#", ".#.#.", "#.#.#"]


def tile_bytes(rows):
    out = bytearray()
    rows = list(rows) + ["....."]  # row 7 blank
    for row in rows[:8]:
        row = row.ljust(8, '.')
        bits = 0
        for i, ch in enumerate(row[:8]):
            if ch == '#':
                bits |= 0x80 >> i
        out += bytes([bits, 0, 0, 0])  # plane 0 only -> colour 1
    return bytes(out)


def main():
    if len(sys.argv) < 2:
        sys.exit("usage: makefont.py OUT.BIN [--preview]")
    data = bytearray()
    for code in range(0x20, 0x60):
        rows = GLYPHS.get(chr(code), MISSING)
        assert len(rows) == 7 and all(len(r) == 5 for r in rows), \
            f"bad glyph {chr(code)!r}"
        data += tile_bytes(rows)
    with open(sys.argv[1], 'wb') as f:
        f.write(data)
    if '--preview' in sys.argv:
        for code in range(0x20, 0x60):
            print(f"--- {chr(code)!r} ---")
            for row in GLYPHS.get(chr(code), MISSING):
                print(row.replace('.', ' ').replace('#', '█'))
    print(f"font: {len(data)} bytes, {len(data)//32} tiles -> {sys.argv[1]}")


if __name__ == '__main__':
    main()
